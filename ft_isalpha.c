#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}

int	main()
{
	int	n = 65;
	printf("n:%i is %i", n, ft_isalpha(n));
}
